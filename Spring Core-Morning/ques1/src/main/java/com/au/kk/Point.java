package com.au.kk;


public class Point {
	private int a;
	private int b;

	public Point(int a, int b) {
		this.a = a;
		this.b = b;
	}

	public int geta() {
		return a;
	}

	public void seta(int a) {
		this.a = a;
	}

	public int getb() {
		return b;
	}

	public void setb(int b) {
		this.b = b;
	}

}